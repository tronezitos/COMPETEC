public class Main
{
    public static void main(String[] args) {
        
        int NumeroAumento = 0;
        int NumeroFinal = 0;
        
        while(NumeroAumento <= 100){
            NumeroFinal = NumeroFinal + NumeroAumento;
            System.out.println(NumeroFinal);
            NumeroAumento++;
        }
        
    }
}
